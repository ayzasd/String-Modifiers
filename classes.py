class string_mod():
    def __init__():
        self.name = "StringMod"
    def count_len(self, string):
        return len(string)
    def mod(self, string, mod):
        string = mod
    def copy(self, string):
        return string
    def mod_arr(self, string, mod, value):
        string[value] = mod
    def copy_mod_arr(self, string, mod, value):
        return string[value] == mod